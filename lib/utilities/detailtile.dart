import 'package:flutter/material.dart';

class Detailtile {
  final String Title;
  final String date;
  final String Genre;
  final String Director;
  final String Actors;
  final String Plot;
  final String poster;
  final String Rating;

  const Detailtile({
    required this.Title,
    required this.date,
    required this.Genre,
    required this.Director,
    required this.Actors,
    required this.Plot,
    required this.poster,
    required this.Rating,
  });
}
